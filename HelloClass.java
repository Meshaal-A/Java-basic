public class HelloClass {
    // Method to print a message
    public void printHelloClass() {
        System.out.println("Hello, I'm inside HelloClass!");
    }

    public static void main(String[] args) {
        // Creating an instance of HelloClass
        HelloClass helloObject = new HelloClass();
        // Calling the printHelloClass method
        helloObject.printHelloClass();
    }
}
